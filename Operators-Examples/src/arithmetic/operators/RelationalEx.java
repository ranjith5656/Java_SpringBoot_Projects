package arithmetic.operators;

public class RelationalEx {

	public static void main(String[] args) {
		 
		int a = 60;
		int b = 80;
		
		System.out.println("a==b : "+ (a==b));
		System.out.println("a!=b : "+ (a!=b));
		System.out.println("a<b : "+ (a<b));
		System.out.println("a>b : "+ (a>b));
		System.out.println("a<=b : "+ (a<=b));
		System.out.println("a>=b : "+ (a>=b));
	}
}
